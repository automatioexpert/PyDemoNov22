"""
FOR LOOP:
    Executing a group  of statements multiple time
    WHEN->If you want to perform an activity in sequence , we use for loop
    SEQUENCE->list,set, tuple, string , range

    SYNTAX:
     for x in sequence:
        -------------------------------------
        -------------------------------------
        -------------------------------------
"""
name = "Steve"
for i in name:
    print(i)

for g in range(10):
    g = g + 1
    print(g)

for r in range(100,1,-10):
    print(r)
